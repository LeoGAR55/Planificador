% Obtenemos la materias del semestre preferido por el estudiante
materias_semestre(Materias):-
    % Semestre elegido por el estudiante (e.g., 'segundo semestre')
    preferencia_semes(Semestre),
    % Recoleccion de las materias. Ahora unifica el segundo argumento con la variable Semestre.
    findall(M, materia(M, Semestre), Materias).

% Obtenemos una clase valida en base a la preferencia de turno y profesor
clase(Materia, Profesor, Salon, Dia, Inicio, Fin) :-
    % 1. Aplica la Restricción de Turno
    preferencia_turno(T),
    horario(Dia, Inicio, Fin, T),

    % 2. Aplica la Restricción de Profesor
    profesor_preferido_para(Materia, Profesor),
    
    % 3. Asigna un Salón (Usando el hecho 'salon/1')
    salon(Salon). % <-- ¡Corregido!

% 3.3. profesor_preferido_para/2: Prioriza al profesor preferido.
profesor_preferido_para(Materia, Profesor) :-
    % Profesor preferido por el estudiante
    preferencia_profe(ListaP),
    % Buscamos si ese profesor aparece en los hechos Profesor
    member(Profesor, ListaP),
    profesor(Profesor, ListaM),
    member(Materia, ListaM),
    !. % Si encuentra un preferido apto, lo elige y corta la búsqueda.

profesor_preferido_para(Materia, Profesor) :-
    % Si no hay preferido o no es apto, elige CUALQUIER otro profesor apto.
    profesor(Profesor, ListaM),
    member(Materia, ListaM).

% 4.1. generar_horario_recursivo/3: Genera combinaciones sin conflictos.
% HorariosAsignados: Lista de [Materia, Profesor, Salon, Dia, Inicio, Fin]
% MateriasPendientes: Lista de materias aún por asignar.

generar_horario_recursivo([], HorariosAsignados, HorariosAsignados). % Caso base: todas asignadas

generar_horario_recursivo([Materia|RestoMaterias], HorariosAcumulados, HorarioFinal) :-
    % 1. Elegir una clase para la Materia
    clase(Materia, Profesor, Salon, Dia, Inicio, Fin),
    
    NuevaClase = [Materia, Profesor, Salon, Dia, Inicio, Fin],

    % 2. Verificar Conflictos (Debe ser NO conflictivo con lo ya acumulado)
    \+ conflicto_clase(NuevaClase, HorariosAcumulados),

    % 3. Llamada recursiva (continuar con el resto de materias)
    generar_horario_recursivo(RestoMaterias, [NuevaClase|HorariosAcumulados], HorarioFinal).


% 4.2. conflicto_clase/2: Verifica si la NuevaClase choca con alguna ya asignada.
conflicto_clase([_, P1, S1, D1, I1, F1], [Clase2|Resto]) :-
    Clase2 = [_, P2, S2, D2, I2, F2],
    % Conflicto de Profesor, Salón o Tiempo
    (   P1 == P2, D1 == D2, \+ (F1 =< I2 ; F2 =< I1) % Mismo profesor en el mismo tiempo
    ;   S1 == S2, D1 == D2, \+ (F1 =< I2 ; F2 =< I1) % Mismo salón en el mismo tiempo
    ),
    !. % Conflicto encontrado.

conflicto_clase(NuevaClase, [_|Resto]) :-
    conflicto_clase(NuevaClase, Resto).


